// PDF01
const btnAbrirModalPDF01 = document.getElementById('btnAbrirModalPDF01');
const modalPDF01 = document.getElementById('modalPDF01');
const btnCerrarModaPDF01 = document.getElementById('btnCerrarModaPDF01');

btnAbrirModalPDF01.addEventListener('click', () => {
    modalPDF01.style.display = 'flex'; 
    modalContent.classList.add('animate__animated', 'animate__fadeInUp'); 
    document.body.style.overflow = 'hidden';
});

btnCerrarModaPDF01.addEventListener('click', () => {
    modalPDF01.style.display = 'none'; 
    document.body.style.overflow = 'auto';
});

modalPDF01.addEventListener('click', (event) => {
    if (event.target === modalPDF01) {
        modalPDF01.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
});

